export default function(firstDate, secondDate) {
    if ( 
        (firstDate.month() === 2 && secondDate.month() === 3) ||
        (firstDate.month() === 5 && secondDate.month() === 6) ||
        (firstDate.month() === 8 && secondDate.month() === 9) ||
        (firstDate.month() === 11 && secondDate.month() === 0))
    {
        return true;
    } else {
        return false;
    }
}
